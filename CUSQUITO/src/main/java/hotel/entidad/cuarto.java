package hotel.entidad;

public class cuarto {

	private int id_cuarto;
	private String nomb_cuart;
	private String tip_cuart;
	private int piso_cuart;
	private Double cost_cuarto;
	
	
	public int getId_cuarto() {
		return id_cuarto;
	}
	public void setId_cuarto(int id_cuarto) {
		this.id_cuarto = id_cuarto;
	}
	public String getNomb_cuart() {
		return nomb_cuart;
	}
	public void setNomb_cuart(String nomb_cuart) {
		this.nomb_cuart = nomb_cuart;
	}
	public String getTip_cuart() {
		return tip_cuart;
	}
	public void setTip_cuart(String tip_cuart) {
		this.tip_cuart = tip_cuart;
	}
	public int getPiso_cuart() {
		return piso_cuart;
	}
	public void setPiso_cuart(int piso_cuart) {
		this.piso_cuart = piso_cuart;
	}
	public Double getCost_cuarto() {
		return cost_cuarto;
	}
	public void setCost_cuarto(Double cost_cuarto) {
		this.cost_cuarto = cost_cuarto;
	}

	
}
